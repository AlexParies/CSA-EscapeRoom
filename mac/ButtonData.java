public class ButtonData {
    public String name;
    public int x;
    public int y;
    public int width;
    public int height;
    public ButtonData(String name, int x, int y, int width, int height) {
        this.name = name;
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
